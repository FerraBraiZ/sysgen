<h3>Escolha um tipo de campo para a FK:</h3>
<br>No grid abaixo cada coluna da tabela/view faz rerencia a outro campo. Na tela será gerado o tipo de campo para FK conforme selecionado. <b>ATENÇÃO</b> Somente o SELECT irá funcionar logo na primeira versão. Nos outros tipos de campos será gerado um SCAFFOLD, com uma pequena intervenção irá funcionar
<ul>
	<li>Select Field: é tipo mais simples é comum. É campo select igual o que aparece no grid</li>
	<li>Autocomplete: Terá um campo texto que servirá de busca para buscar um item</li>
