<h3>Orientações sobre a estrutura de banco de dados</h3>

<br> Essas orientações não são obrigações! Se seguir terá menos trabalho e para arrumar o código gerado.

<ul>
	<li>Evite chaves primarias compostas. Procure utilizar sempre a chaves primaria simples com AUTOINCREMENT. Transforme as chaves compostas em chaves candidatas com Unique Key. Veja mais informações em <a href="https://www.devmedia.com.br/forum/qual-a-diferenca-entreprimary-key-e-unique-key/513038" target="_blank">Qual a diferença entre Primary Key e Unique Key ?</a></li>
	<li>Faça descrições dos campos da tabela, assim eles poderão virá ajuda</li>
</ul>

<h3>Campos:</h3>
<ul>
	<li>Sigla do Sistema: A sigla do sistema é o ID do sistema. Será utiliza para criar a pasta do novo sistema. Por padrão aparecerá no canto superior esquerdo</li>
	<li>Nome do sistema: É descrição completa do nome do sistema ou o que significa a sigla</li>
</ul>