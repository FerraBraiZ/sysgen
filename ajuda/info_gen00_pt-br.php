<h3>Orientações sobre a estrutura de banco de dados</h3>

<br> Essa orientações não são obrigações ! Se seguir terá menos trabalho e para arrumar o codigo gerado.

<ul>
	<li>Evite chaves primarias compostas. Procure utilizar sempre chaves simples com autoincremente. Transforme as chaves compostas em chaves candiadatas com unique key.</li>
	<li>Faça descrições do campos das tabela, assim eles poderam virá ajuda</li>
</ul>

<h3>Campos:</h3>
<ul>
	<li>Sigla do Sistema: A sigla do sistema é o ID do sistema. Será utiliza para criar a pasta do novo sistema. Por padrão irá aparecer no canto superior esquerdo</li>
	<li>Nome do sistem: É descrição completa do nome do sistema ou o que significa a sigla</li>
</ul>